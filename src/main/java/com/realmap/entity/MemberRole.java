package com.realmap.entity;

/**
 * 회원의 서비스 교환 역할을 정의하는 열거형.
 *
 * <p>realMap 플랫폼에서 회원은 서비스를 제공하는 사람, 받는 사람, 또는 양방향 역할을 수행할 수 있습니다.
 * 이 역할은 회원 가입 시 설정되며, 상대방 검색 필터링에도 사용됩니다.</p>
 *
 * <p>예시 시나리오:
 * <ul>
 *   <li>웹 디자이너가 자신의 디자인 서비스를 제공하려면 → {@code PROVIDER}</li>
 *   <li>영어 번역 서비스를 받고 싶은 사람 → {@code RECEIVER}</li>
 *   <li>코딩도 가르쳐주고 요리도 배우고 싶은 사람 → {@code BOTH}</li>
 * </ul>
 * </p>
 */
public enum MemberRole {

    /**
     * 서비스 제공자.
     * 자신의 재능, 기술, 능력을 상대방에게 제공하는 역할.
     * 예: "웹 디자인을 해드릴 수 있어요", "영어 번역 가능해요"
     */
    PROVIDER,

    /**
     * 서비스 수혜자.
     * 상대방의 재능, 기술, 능력으로부터 서비스를 받는 역할.
     * 예: "개발자 멘토링을 받고 싶어요", "요리를 배우고 싶어요"
     */
    RECEIVER,

    /**
     * 양방향 역할.
     * 같은 플랫폼에서 서비스를 제공하기도 하고 받기도 하는 사람.
     * 상호 무페이 교환의 핵심 참여자 유형 — "나는 A를 줄 테니 B를 주세요"
     */
    BOTH
}
